def get_dynamic_prompt(
    skill: SkillType,
    user_message: str,
    include_hints: bool = True,
) -> str:
    """根据用户消息动态生成增强 Prompt

    核心 Prompt + 场景提示 = 精确指导，减少无关 Token

    Args:
        skill: 技能类型
        user_message: 用户消息
        include_hints: 是否注入场景提示

    Returns:
        组合后的完整 System Prompt
    """
    base_prompt = SKILL_PROMPTS.get(skill, SKILL_PROMPTS[SkillType.WRITING])

    if not include_hints:
        return base_prompt

    # 匹配场景提示
    hints = DYNAMIC_HINTS.get(skill, {})
    matched_hints: list[str] = []

    for keyword, hint_text in hints.items():
        if keyword in user_message:
            matched_hints.append(hint_text)

    # 最多注入 3 个提示，避免 Prompt 过长
    if matched_hints:
        return base_prompt + "".join(matched_hints[:3])

    return base_prompt


def estimate_prompt_tokens(prompt: str) -> int:
    """估算 Prompt 的 Token 数（中文约 1.5 字符/Token）"""
    chinese_chars = sum(1 for c in prompt if '\u4e00' <= c <= '\u9fff')
    other_chars = len(prompt) - chinese_chars
    return int(chinese_chars / 1.5 + other_chars / 3.5)
